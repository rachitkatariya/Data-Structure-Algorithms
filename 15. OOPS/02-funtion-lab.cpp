#include<iostream>
using namespace std;

int main()
{
    cout<<"size by data types"<<endl;
    cout<<sizeof(int)<<endl;
    cout<<sizeof(float)<<endl;
    cout<<sizeof(char)<<endl;

    cout<<endl;

    // size by value

    cout<<"size by value"<<endl;

    cout<<sizeof(40)<<endl;
    cout<<sizeof(4.5)<<endl;
    cout<<sizeof('4')<<endl;
    cout<<sizeof(40000)<<endl;

    cout<<sizeof(4.5f);
}